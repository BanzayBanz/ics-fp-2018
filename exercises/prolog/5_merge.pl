% определяет список R, составленный из этих элементов

mrg([], [], []).
mrg([], List, List).
mrg(List, [], List).

mrg([H1|T1], [H2|T2], [H1|TRes]) :-
    H1 < H2, mrg(T1, [H2|T2], TRes), !.

mrg([H1|T1], [H2|T2], [H2|TRes]) :-
mrg([H1|T1], T2, TRes).

