% определить предикат mrg(L1, L2, R) который для двух отсортированных списков L1 и L2 
% определяет список R, составленный из этих элементов

mrg(L1,[],L1).
mrg([],L2,L2).
mrg([L1|Rl1],[L2|Rl2],[L1|M]) :-  L1 == L2, mrg(Rl1,Rl2,M).
mrg([L1|Rl1],[L2|Rl2],[L1|M]) :-  L1 < L2, mrg(Rl1,[L2|Rl2],M).
mrg([L1|Rl1],[L2|Rl2],[L2|M]) :-  L1 > L2,  mrg([L1|Rl1],Rl2,M).

